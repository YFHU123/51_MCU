unsigned char Key_Read()
{
    unsigned char temp = 0;

    // --- ЩЈУшЕк 1 СаЃКP3.0 жУЕЭ---
    P3 = 0xFE; //1111 1110
    if((P3 & 0x10) == 0) temp = 1;  // МьВт P3.4 (S1)
    if((P3 & 0x20) == 0) temp = 2;  // МьВт P3.5 (S2)
    if((P3 & 0x40) == 0) temp = 3;  // МьВт P3.6 (S3)
    if((P3 & 0x80) == 0) temp = 4;  // МьВт P3.7 (S4)

    // --- ЩЈУшЕк 2 СаЃКP3.1 жУЕЭ---
    P3 = 0xFD; // 1111 1101
    if((P3 & 0x10) == 0) temp = 5;  // МьВт P3.4 (S5)
    if((P3 & 0x20) == 0) temp = 6;  // МьВт P3.5 (S6)
    if((P3 & 0x40) == 0) temp = 7;  // МьВт P3.6 (S7)
    if((P3 & 0x80) == 0) temp = 8;  // МьВт P3.7 (S8)

    // --- ЩЈУшЕк 3 СаЃКP3.2 жУЕЭ---
    P3 = 0xFB; //1111 1011
    if((P3 & 0x10) == 0) temp = 9;  // МьВт P3.4 (S9)
    if((P3 & 0x20) == 0) temp = 10; // МьВт P3.5 (S10)
    if((P3 & 0x40) == 0) temp = 11; // МьВт P3.6 (S11)
    if((P3 & 0x80) == 0) temp = 12; // МьВт P3.7 (S12)

    // --- ЩЈУшЕк 4 СаЃКP3.3 жУЕЭ---
    P3 = 0xF7; //1111 0111
    if((P3 & 0x10) == 0) temp = 13; // МьВт P3.4 (S13)
    if((P3 & 0x20) == 0) temp = 14; // МьВт P3.5 (S14)
    if((P3 & 0x40) == 0) temp = 15; // МьВт P3.6 (S15)
    if((P3 & 0x80) == 0) temp = 16; // МьВт P3.7 (S16)

    return temp;
}

